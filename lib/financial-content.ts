export const getTipByLanguage = (tips: Record<string, string>, language: string) => {
  return tips[language as keyof typeof tips] || tips.en
}

export const getLessonContent = (lessonId: string, language: string) => {
  for (const category of Object.values(financialLessons)) {
    const lesson = category.lessons?.find((l) => l.id === lessonId)
    if (lesson) {
      return {
        title: lesson.title[language as keyof typeof lesson.title] || lesson.title.en,
        description: lesson.description[language as keyof typeof lesson.description] || lesson.description.en,
        content: lesson.content[language as keyof typeof lesson.content] || lesson.content.en,
        tips: lesson.tips?.map((tip) => getTipByLanguage(tip, language)) || [],
      }
    }
  }
  return null
}
